def get_arrays(d):
    a=[(k, v) for k, v in d.iteritems()]
    a.sort()
    return [i[0] for i in a], [i[1] for i in a]


def plot_dicts(a, b, filename):
    ak, av=get_arrays(a)
    bk, bv=get_arrays(b)
    plt.plot(ak, av, "r", bk, bv, "b")
    plt.savefig(filename)
    plt.show()

